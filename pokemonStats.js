/**
 * Fonction pour obtenir les stats de base d'un Pokémon
 */
function getPokemonBaseStats(name, level) {
    const levelMultiplier = 1 + (level * 0.1);
    
    // Si le Pokémon a des stats personnalisées, les utiliser
    if (POKEMON_BASE_STATS[name]) {
        const base = POKEMON_BASE_STATS[name];
        return {
            hp: Math.floor(base.hp *2* levelMultiplier),
            attack: Math.floor(base.attack * levelMultiplier),
            defense: Math.floor(base.defense * levelMultiplier),
            speed: Math.floor(base.speed * levelMultiplier)
        };
    }
    
    // Sinon, utiliser des stats génériques basées sur le niveau
    return {
        hp: 1,
        attack: 1,
        defense:1,
        speed: 1
    };
}

